import streamlit as st

# 앱의 제목을 설정합니다.
st.title("MBTI 성격 유형 검사")

# 설명을 추가합니다.
st.write("아래의 각 질문에 대해 자신에게 가장 가까운 옵션을 선택해주세요.")

# 질문과 옵션 설정
E_I = st.radio(
    "당신은 주로 어디에서 에너지를 얻나요?",
    ('외향(E) - 다른 사람들과 어울리며 에너지를 얻습니다.', 
     '내향(I) - 혼자 있는 시간을 통해 에너지를 얻습니다.')
)

S_N = st.radio(
    "당신은 정보를 어떻게 처리하나요?",
    ('감각(S) - 구체적이고 현재의 정보에 집중합니다.', 
     '직관(N) - 가능성과 미래의 추상적 정보에 집중합니다.')
)

T_F = st.radio(
    "의사 결정을 할 때 주로 무엇을 중시하나요?",
    ('사고(T) - 논리적이고 일관된 결정을 선호합니다.', 
     '감정(F) - 사람들의 감정을 고려하고 조화를 중시합니다.')
)

J_P = st.radio(
    "생활 방식은 어떠한가요?",
    ('판단(J) - 계획적이고 조직적입니다.', 
     '인식(P) - 유연하고 즉흥적입니다.')
)

# 결과 계산 버튼
if st.button('결과 보기'):
    # 사용자의 선택을 기반으로 MBTI 유형을 결정합니다.
    result = ""
    result += 'E' if '외향(E)' in E_I else 'I'
    result += 'S' if '감각(S)' in S_N else 'N'
    result += 'T' if '사고(T)' in T_F else 'F'
    result += 'J' if '판단(J)' in J_P else 'P'
    
    # 결과 출력
    st.subheader(f"당신의 MBTI 유형은 {result}입니다.")
    st.write("이 유형은 단지 참고용이며, 사람의 복잡한 성격을 완전히 설명할 수는 없습니다.")

